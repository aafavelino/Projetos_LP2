	public class Rodas
	{
		public int tamanho;
		public int pressao;

		Rodas()
		{
			tamanho = 15;
			pressao = 28;
		}

		public int getTamanho()
		{
			return tamanho;
		}

		public void setTamanho(int _tamanho)
		{
				this.tamanho = _tamanho;
		}

		public int getPressao()
		{
			return pressao;
		}

		public void setPressao(int _pressao)
		{
				this.pressao = _pressao;
		}
	}